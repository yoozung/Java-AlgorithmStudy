/*
1. 문자 찾기

-설명
한 개의 문자열을 입력받고, 특정 문자를 입력받아 해당 특정문자가 입력받은 문자열에 몇 개 존재하는지 알아내는 프로그램을 작성하세요.
대소문자를 구분하지 않습니다.문자열의 길이는 100을 넘지 않습니다.

-입력
첫 줄에 문자열이 주어지고, 두 번째 줄에 문자가 주어진다.
문자열은 영어 알파벳으로만 구성되어 있습니다.

-출력
첫 줄에 해당 문자의 개수를 출력한다.
*/

import java.util.*;
class  Main{
	public int solution(String str, char t){
		int answer=0;
		str = str.toUpperCase(); //.toUpperCase(): 대문자로 변환
		t = Character.toUpperCase(t);
		//System.out.println(str + "" + t);

		/* for 사용
		for(int i = 0; i < str.length(); i++) {
			if(str.charAt(i) == t) answer++; //.charAt(i) 인덱스를 문자열로 접근 
		} */

		/*foreach 사용*/
		for(char x : str.toCharArray()) { //.toCharArray(): x에 대응하는 문자배열 생성
		   if(x==t) answer++;
		}
		return answer;
	}


	public static void main(String[] args) {
		Main T = new Main();
		Scanner kb = new Scanner(System.in);
		String str = kb.next(); // .next: 스캐너로 문자열 하나 읽어들이는 것
		char c = kb.next().charAt(0); // .next().charAt(0): 문자열에서 0번째 있는 항목을 가져와라

		System.out.println(T.solution(str, c));
	}
}
